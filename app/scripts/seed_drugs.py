async def seed():
    await Tortoise.init(
        db_url=DATABASE_URL,
        modules={"models": ["app.models.drug_document"]},
    )
    await Tortoise.generate_schemas()

    created = 0
    skipped = 0

    for drug in SEED_DATA:
        for section, content in drug["sections"].items():
            _, is_created = await DrugDocument.get_or_create(
                drug_name=drug["drug_name"],
                section=section,
                defaults={
                    "drug_name_en": drug.get("drug_name_en"),
                    "content": content,
                    "source": drug.get("source"),
                },
            )
            if is_created:
                created += 1
            else:
                skipped += 1

    await Tortoise.close_connections()
    print(f"시드 완료: {created}건 생성, {skipped}건 스킵 (이미 존재)")


if __name__ == "__main__":
    asyncio.run(seed())
